package com.example.newpuzzlegame;

import com.example.newpuzzlegame.model.Block;

import java.util.ArrayList;
import java.util.List;


public class KlotskiMapParser {

    // 地图字符串
    //    String str = "" +
    //            "4,       0,      0," +
    //            "3,       2,      0," +
    //            "3,       2,      1," +
    //            "1,       2,      2," +
    //            "1,       3,      2," +
    //            "2,       0,      3," +
    //            "2,       1,      3," +
    //            "1,       2,      3," +
    //            "2,       3,      3," +
    //            "1,       2,      4";
    //            type      x       y
    //
    //        ┌-------------> x
    //        | ┌---------------┬-------┬-------┐
    //        | |///////////////|///////|///////|
    //        | |///////////////|///1///|///////|
    //        | |///////////////|///////|///////|
    //        | |///////4///////├-------┤///3///|
    //        | |///////////////|       |///////|
    //       y| |///////////////|       |///////|
    //        V |///////////////|       |///////|
    //          ├-------┬-------┼-------┴-------┤
    //          |       |///////|///////////////|
    //          |       |///////|///////2///////|
    //          |       |///////|///////////////|
    //          ├-------┤///3///├---------------┤
    //          |///////|///////|///////////////|
    //          |///1///|///////|///////2///////|
    //          |///////|///////|///////////////|
    //          ├-------┼-------┼---------------┤
    //          |///////|///////|///////////////|
    //          |///1///|///1///|///////2///////|
    //          |///////|///////|///////////////|
    //          └-------┴-------┴---------------┘
    //                  └-----exit------┘

    public static List<Block> parse(String map) {

        List<Block> blocks = new ArrayList<>();

        String[] temp = map.split(",");
        int count = temp.length / 3;

        for (int i = 0; i < count; i++) {
            int type = Integer.parseInt(temp[i * 3]);
            int x = Integer.parseInt(temp[i * 3 + 1]);
            int y = Integer.parseInt(temp[i * 3 + 2]);
            blocks.add(new Block(Block.Type.parse(type), x, y, null));
        }

        return blocks;
    }
}

